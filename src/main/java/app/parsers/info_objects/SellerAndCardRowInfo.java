package app.parsers.info_objects;

public class SellerAndCardRowInfo {
	
	private String sellCount;
	private String availableCardAmount;
	private String country;
	private String sellerName;
	private String sellerType;
	private String cardCondition;
	private String cardLanguage;
	private boolean isSigned;
	private boolean isPlayset;
	private String price;
	private String cardCopiesAmount;
	
	
	public String getSellCount() {
		return sellCount;
	}
	public void setSellCount(String sellCount) {
		this.sellCount = sellCount;
	}
	public String getAvailableCardAmount() {
		return availableCardAmount;
	}
	public void setAvailableCardAmount(String availableCardAmount) {
		this.availableCardAmount = availableCardAmount;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getSellerName() {
		return sellerName;
	}
	public void setSellerName(String sellerName) {
		this.sellerName = sellerName;
	}
	public String getSellerType() {
		return sellerType;
	}
	public void setSellerType(String sellerType) {
		this.sellerType = sellerType;
	}
	public String getCardCondition() {
		return cardCondition;
	}
	public void setCardCondition(String cardCondition) {
		this.cardCondition = cardCondition;
	}
	public String getCardLanguage() {
		return cardLanguage;
	}
	public void setCardLanguage(String cardLanguage) {
		this.cardLanguage = cardLanguage;
	}
	public boolean isSigned() {
		return isSigned;
	}
	public void setSigned(boolean signed) {
		this.isSigned = signed;
	}
	public boolean isPlayset() {
		return isPlayset;
	}
	public void setPlayset(boolean isPlayset) {
		this.isPlayset = isPlayset;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getCardCopiesAmount() {
		return cardCopiesAmount;
	}
	public void setCardCopiesAmount(String cardCopiesAmount) {
		this.cardCopiesAmount = cardCopiesAmount;
	}


}
